package com.hyrTutorials.variables;

public class VariableNamingConvention {
    static String name ="Divya";
    static String NAME = "DIVYA";
    static String firstName = "KC";
    static String first_name = "sowmya";
    static int money$ = 50;
    static double MATH_PI = 3.14;
    public static void main(String[] args){
        System.out.println(name);
        System.out.println(NAME);
        System.out.println(firstName);
        System.out.println(first_name);
        System.out.println(MATH_PI);
        System.out.println(money$);

    }

}
